# ------------------------------------------------- regular imports -------------------------------------------------- #

from typing   import Optional
from pydantic import BaseModel, HttpUrl

# -------------------------------------------------------------------------------------------------------------------- #

class BadgeProps(BaseModel): # ....................................................................................... #
    text: str
# end ..................................................................................................... BadgeProps #

class TechStack(BaseModel): # ........................................................................................ #
    text: str
    alt:  str
    href: HttpUrl
# end ...................................................................................................... TechStack #

class DataItem(BaseModel): # ......................................................................................... #
    img:         str
    alt:         str
    site:        HttpUrl
    title:       str
    source:      Optional[HttpUrl | str] = None
    badge_props: list[BadgeProps]
    tech_stack:  list[TechStack]
    description: str
# end ....................................................................................................... DataItem #
